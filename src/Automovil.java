public class Automovil {
	private String marca;
	private String modelo;
	private int añoFrabricacion;
	private double precio;
	public Motor motorAuto;
	public Chasis chasisAuto;

	public String getMarca() {
		return this.marca;
	}

	public String getModelo() {
		return this.modelo;
	}

	public int getAñoFrabricacion() {
		return this.añoFrabricacion;
	}

	public double getPrecio() {
		return this.precio;
	}
	public Motor getMotorAuto() {
		return motorAuto;
	}

	public Chasis getChasisAuto() {
		return chasisAuto;
	}


}